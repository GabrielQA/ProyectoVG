    public void veriPass(String[] h) {
        ArchUsu login = new ArchUsu();

        h = login.extraerPass();

        if ((RS1.getText().equals(h[0])) & (RS2.getText().equals(h[1]))) {
            System.out.println("Correcto");
            TiendaGV O = new TiendaGV();
            O.setVisible(true);
            O.setEnabled(true);
            O.setLocationRelativeTo(null);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "No coinsiden");
        }
    }

